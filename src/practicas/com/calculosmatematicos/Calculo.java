package practicas.com.calculosmatematicos;

public class Calculo {
	private static int resultado;

	public static int suma_numero(int a, int b) {

		resultado = a + b;

		return resultado;
	}

	public static int resta_numero(int a, int b) {

		resultado = a - b;

		return resultado;
	}

	public static int multiplica_numero(int a, int b) {

		resultado = a * b;

		return resultado;
	}

}
